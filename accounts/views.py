def add_user(request):
	template_name = 'create_update.html'
	context = {}
	if request.method == 'POST':
		form = UserForm(request.POST)
		if form.is_valid():
			f = form.save(commit=False)
			f.set_password(f.password)
			f.save()
	form = UserForm()
	context['form'] = form
	return render(request, template_name, context)

